package nuaa.casting.service;

public class BMpropara {
	private String castingmodeling;
	private String sizecondition;
	private String draftangle;
	private String draftlength;
	private String draftremarks;
	public String getCastingmodeling() {
		return castingmodeling;
	}
	public void setCastingmodeling(String castingmodeling) {
		this.castingmodeling = castingmodeling;
	}
	public String getSizecondition() {
		return sizecondition;
	}
	public void setSizecondition(String sizecondition) {
		this.sizecondition = sizecondition;
	}
	public String getDraftangle() {
		return draftangle;
	}
	public void setDraftangle(String draftangle) {
		this.draftangle = draftangle;
	}
	public String getDraftlength() {
		return draftlength;
	}
	public void setDraftlength(String draftlength) {
		this.draftlength = draftlength;
	}
	public String getDraftremarks() {
		return draftremarks;
	}
	public void setDraftremarks(String draftremarks) {
		this.draftremarks = draftremarks;
	}
	
	
}
